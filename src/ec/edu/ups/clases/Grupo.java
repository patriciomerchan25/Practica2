package ec.edu.ups.clases;

/**
 *
 * @author Usuario-Pc
 */
public class Grupo {
    
    private int codigo;
    private String combre;
    private int cupo;

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public void setCombre(String combre) {
        this.combre = combre;
    }

    public void setCupo(int cupo) {
        this.cupo = cupo;
    }

    public int getCodigo() {
        return codigo;
    }

    public String getCombre() {
        return combre;
    }

    public int getCupo() {
        return cupo;
    }
    
    
    
}
